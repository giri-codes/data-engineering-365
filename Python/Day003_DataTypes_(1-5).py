# Day 03 - Data Types (1-5)

# Program 1 - Check Data Type of Integer
num = 100
print(type(num))


# Program 2 - Check Data Type of Float
salary = 50000.50
print(type(salary))


# Program 3 - Check Data Type of String
name = "Giri"
