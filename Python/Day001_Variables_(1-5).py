# Day 001 - Variables(1-5)


# Program 1 - Store Your Name
name = "Gireesh"
print("Name:",name)


# Program 2 - Store Age and City
age = 28
city = "Mysuru"
print("Age:",age)
print("City:",city)


# Program 3 - Create Multiple Variables
name = "Gireesh"
age = 28
city = "Mysuru"
salary = 250000

print("Name:",name)
print("Age:",age)
print("City:",city)
print("Salary:",salary)

# Program 4 - Reassign Variable
salary = 100000
print("Old Salary:", salary)
salary = 250000
print("New Updated Salary:", salary)


# Program 5 - Swap Two Variables
first_number = 10
second_number = 20
print("Before Swap")
print("first_number =", first_number)
print("second_number =", second_number)

first_number, second_number = second_number, first_number

print("After Swap")
print("first_number =", first_number)
print("second_number =", second_number)