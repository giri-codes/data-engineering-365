# Python Foundations
# Topic - Variables
# Day 001 - Variables(1-5)


# Program 1 - Store Your Name
name = "Gireesh"
print(name)


# Program 2 - Store Age and City
age = 28
city = "Mysuru"
print(age)
print(city)


# Program 3 - Create Multiple Variables
name = "Gireesh"
age = 28
city = "Mysuru"

print(name)
print(age)
print(city)


# Program 4 - Reassign Variable
salary = 45000
print("Old Salary:", salary)
salary = 50000
print("New Salary:", salary)


# Program 5 - Swap Two Variables
a = 10
b = 20
print("Before Swap")
print("a =", a)
print("b =", b)

a, b = b, a

print("After Swap")
print("a =", a)
print("b =", b)